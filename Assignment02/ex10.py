#\t creates a tab
tabby_cat = "\tI'm tabbed in."
#\n creates a line break
persian_cat = "I'm split\non a line."
#\\ inserts a single backslash so that it does not register as a syntax error
backslash_cat = "I'm \\ a \\ cat."

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)

